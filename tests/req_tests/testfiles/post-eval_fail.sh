#!/usr/bin/env bash

# post exceeds maxBodySize for the route -> should 413

# ---------------------------=[ test boilerplate ]=--------------------------- #

if [ $# -lt 2 ]; then
	echo "need at least 2 args: [-n] addr port"
	exit 1
fi

if [[ $# -eq 3 && "$1" != "-n" ]]; then
	echo "usage with 3 args: $0 -n addr port"
	exit 1
fi

if [ ! -e ../_test_utils.sh ]; then
	echo "test-utils not found!"
	exit 1
else
	source ../_test_utils.sh
fi

set -u

if [ $# -eq 3 ]; then
	hostname="$2"
	port="$3"
else
	hostname="$1"
	port="$2"
	$webserv $cfgDir/bigFileUpload.wsrv > /dev/null &
	sleep 0.1s
fi

# ------------------------=[ test logic starts here ]=------------------------ #

RESPONSE=$(curl -s -X POST -H "Content-Type: plain/text" --data "BODY IS HERE write something shorter or longer than body limit" localhost:1234/small | grep 413)

echo "Response:"
echo "---------"
echo "${RESPONSE[@]}"

exec 3<&-

# SIGINT kill webserv
if [ $# -eq 2 ]; then
	pkill -INT webserv
fi

if [ -z "$RESPONSE" ]; then
	exit 1;
fi

